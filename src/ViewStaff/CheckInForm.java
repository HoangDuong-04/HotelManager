    private void btnCheckInActionPerformed(java.awt.event.ActionEvent evt) {
        // GEN-FIRST:event_btnCheckInActionPerformed
        String cusID = txtCusID.getText();
        String cusName = txtCusName.getText();
        String cusPhoneNum = txtCusPhoneNum.getText();
        String cusSex = (CbGender.getSelectedItem() != null) ? CbGender.getSelectedItem().toString() : "";
        String roomID = (CbRoomID.getSelectedItem() != null) ? CbRoomID.getSelectedItem().toString() : "";

        if (cusID.isEmpty() || cusName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Not enough information!");
            return;
        }

        try (Connection conn = cn.getConnection()) {
            String sql = "SELECT * FROM Customer WHERE CusID = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, cusID);
            ResultSet rsCustomer = pstmt.executeQuery();

            if (!rsCustomer.next()) {
                String sql_Customer = "INSERT INTO Customer (CusID, CusName, CusPhone, CusGender) VALUES (?, ?, ?, ?)";
                PreparedStatement ps_customer = conn.prepareStatement(sql_Customer);
                ps_customer.setString(1, cusID);
                ps_customer.setString(2, cusName);
                ps_customer.setString(3, cusPhoneNum);
                ps_customer.setString(4, cusSex);
                ps_customer.executeUpdate();
            } else {
                JOptionPane.showMessageDialog(this, "Customer already exists!");
            }

            String sql_Enroll = "INSERT INTO Enroll (EnrollID, CusID, RoomID,CheckInDate) VALUES (?, ?, ?, ?)";
            PreparedStatement ps_enroll = conn.prepareStatement(sql_Enroll);
            ps_enroll.setString(2, cusID);
            ps_enroll.setString(3, roomID);

            java.util.Date date = txtDateCheckIn.getDate();
            if (date != null) {
                java.sql.Date sqlDate = new java.sql.Date(date.getTime());
                ps_enroll.setDate(4, sqlDate);
                ps_enroll.setString(1, cusID + "_" + roomID + "_" + sqlDate.toString());
            } else {
                ps_enroll.setNull(4, java.sql.Types.DATE);
            }

            if (ps_enroll.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this, "Check In Successfully!");
                updateRoomStatus();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }// GEN-LAST:event_btnCheckInActionPerformed

