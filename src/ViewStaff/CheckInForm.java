    private void updateRoomStatus() {
        String selectValueID = CbRoomID.getSelectedItem().toString();
        String sql = "UPDATE Room SET RoomStatus = 'Booked' WHERE RoomID = ?";

        try (Connection conn = cn.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setString(1, selectValueID);
            int rowsAffected = ps.executeUpdate();

            if (rowsAffected <= 0) {
                JOptionPane.showMessageDialog(this, "Can't update!");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void findRoomByType() {
        String selectValueType = CbRoomType.getSelectedItem().toString();
        String sql = "SELECT RoomID FROM Room WHERE RoomType = ? AND RoomStatus = 'Not Booked'";

        try (Connection conn = cn.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setString(1, selectValueType);
            ResultSet rs = ps.executeQuery();

            CbRoomID.removeAllItems();
            while (rs.next()) {
                CbRoomID.addItem(rs.getString("RoomID"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void updateDate() {
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Calendar currentDate = Calendar.getInstance();
            String formattedDate = sdf.format(currentDate.getTime());
            txtDateCheckIn.setDateFormatString("yyyy-MM-dd");
            txtDateCheckIn.setDate(sdf.parse(formattedDate));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    public void addPrice() {
        String selectValueID = (CbRoomID.getSelectedItem() != null) ? CbRoomID.getSelectedItem().toString() : "";
        String sql_price = "SELECT RoomPrice FROM Room WHERE RoomID = ?";

        try (Connection conn = cn.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql_price)) {

            ps.setString(1, selectValueID);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                txtRoomPrice.setText(rs.getString("RoomPrice"));
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

